    public static boolean isVariable(String var, Ambitos ambito) {
        Object aux1 = ambito.TablaSimbolos.get(var);

        if (aux1 != null) {
            return true;
        }

        for (Ambitos a : ambito.getHijos()) {
            Object aux = a.TablaSimbolos.get(var);
            if (aux != null) {
                return true;
            } else {
                return isVariable(var, a);
            }
        }
        return false;
    }

